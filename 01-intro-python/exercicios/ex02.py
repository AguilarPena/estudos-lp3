# Exercíco 02: escreva um programa em Python que solicita ao usuário três números e apresenta a média aritmética dos números informados.

numero1 = int(input('Digite o primeiro número inteiro: '))
numero2 = int(input('Digite o segundo número inteiro: '))
numero3 = int(input('Digite o terceiro número inteiro: '))

media = (numero1 + numero2 + numero3)/3

print('A média aritmética dos valores é:', media)